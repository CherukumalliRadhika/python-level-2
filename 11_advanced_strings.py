name = "Radhika"
age = 22

# normal concatenation
print("My name is " + name + " and I am " + str(age) + " years old.")

# f-string (easier!)
print(f"My name is {name} and I am {age} years old.")

# string methods
message = "   hello python basics   "
print(message.strip())       # remove spaces
print(message.upper())       # HELLO PYTHON BASICS
print(message.title())       # Hello Python Basics
